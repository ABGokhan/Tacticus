          <h5>Search Engine Optimisation</h5>
          <p>Search engine optimization (SEO), is the process of increasing website traffic from search engines. Search engine optimization targets non-paid traffic, also known as organic or natural results. It is not intended to target direct traffic or paid visitors. Search engine optimization can target unpaid traffic from different types of searches such as image, video, news, academic, and vertical search engines. Search engine optimization is an Internet marketing strategy that considers the way search engines work, how they behave, the computer-programmed algorithms which control search engine behavior, what people are searching for, the search terms and keywords used in search engines and which search engines are most preferred by their target audience. Because websites that rank higher in search engine results pages (SERP) will attract more traffic, search engine optimization is done. These visitors could then be converted into customers.</p>
        </li>
