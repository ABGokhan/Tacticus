          <h5>Strategic Marketing</h5>
          <p>A marketing strategy can help an organization focus its resources on the best opportunities to increase sales and gain a competitive advantage. Before strategizing, a company must analyze its strategic position before it can formulate, evaluate and select a market-oriented competitive position that will help them achieve their marketing goals. The study of strategic marketing is a separate field and was developed from the earlier areas of strategic management. Marketing strategy emphasizes the importance of marketing as a link between an organization and its customers. Marketing strategy combines the resources and capabilities of an organization to gain a competitive advantage that enhances firm performance.</p>
