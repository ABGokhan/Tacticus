          <h5>Marketing Analytics</h5>
          <p>Marketing analytics refers to the process of measuring, managing, and analysing marketing performance in order to maximize effectiveness and optimize return-on-investment (ROI). Marketing analytics can help marketers be more efficient and reduce wasted marketing dollars by understanding how they work. Marketing analytics provides insight into customer preferences and trends that can be used to generate leads and sales. These compelling benefits aside, most organizations don't realize the potential of marketing analytics because they aren't able to measure ROI.</p>
